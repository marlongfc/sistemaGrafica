    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try {
            if (bairro == null) {
                bairro = new Bairro();
                bairro.setDataCadastro(new Date());
                bairro.setDataAtualizacao(new Date());
            } else {
                bairro.setDataAtualizacao(new Date());
            }
            bairro.setCodBairro(Long.parseLong(codBairro.getText()));
            bairro.setDescricao(descBairro.getText());

            if (bairroDao.salvar(bairro) != null) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
            }

            codBairro.setEnabled(true);
            atualizarTabela();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao salvar bairro. Erro: " + e);
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        try {
            if (bairro == null) {
                throw new Exception("Erro: bairro inválido!");
            }
            int op = 0;
            JOptionPane.showConfirmDialog(null, "Deseja realmente excluir bairro selecionado?", "CONFIRMAÇÃO DE EXCLUSÃO", op);

            // considerando 0 como sim
            if (op == 0) {

                bairroDao.delete(bairro);

                codBairro.setText("");
                descBairro.setText("");
                atualizarTabela();

            } else {
                return;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao excluir bairro. Erro: " + e);
        }
    }//GEN-LAST:event_btExcluirActionPerformed


