    private void codBairroFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_codBairroFocusLost
        try {
            bairro = bairroDao.getPorCodigo(ValidarValor.getLong(codBairro.getText()));
            if (bairro != null) {
                descBairro.setText(bairro.getDescricao());
            } else {
                bairro = new Bairro();
                descBairro.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }        // TODO add your handling code here:
    }//GEN-LAST:event_codBairroFocusLost

    private void descBairroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_descBairroKeyReleased
        try {
            List<Bairro> merged = bairroDao.getList(15, "select e from Bairro e where 1=1 and REPLACE(REPLACE(REPLACE(trim(e.descricao),'.',''),'-',''),' ', '') "
                    + " like ?1 order by e.descricao", descBairro.getText().trim().toLowerCase().replace(".", "").replace("-", "").trim() + "%");
            lista.clear();
            lista.addAll(merged);
        } catch (Exception e) {
            System.out.println("Ocorreu um erro ao tentar pesquisar bairros. Erro: " + e);
        }
    }//GEN-LAST:event_descBairroKeyReleased

    private void atualizarTabela() {

        DefaultTableModel model = (DefaultTableModel) tabBairro.getModel();
        removeLinhas(tabBairro);

        List<?> lv;

        lv = cnvBairro.getLista();

        if (lv != null && !lv.isEmpty()) {

            for (Object o : lv) {
                Object[] os = (Object[]) o;
                model.addRow(os);
                //Colocar tamanho nas colunas

            }
        }
    }

    public static void removeLinhas(JTable table) {
        int n = table.getRowCount();

        DefaultTableModel model = (DefaultTableModel) table.getModel();

        for (int i = n - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }


